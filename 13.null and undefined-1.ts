// null and undefined types

let whatIsIt1: number | null = null;
console.log(whatIsIt1); //hover on whatIsIt1 → null
whatIsIt1 = 123;
console.log(whatIsIt1); //hover on whatIsIt1 → number

let whatIsIt2: number | undefined = undefined;
console.log(whatIsIt2); //hover on whatIsIt2 → undefined
whatIsIt2 = 456;
console.log(whatIsIt2); //hover on whatIsIt2 → number
